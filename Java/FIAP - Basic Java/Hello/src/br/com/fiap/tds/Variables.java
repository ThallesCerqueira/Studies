package br.com.fiap.tds;

public class Variables {

    public static void main(String[] args) {

        int a = 1;
        double c = 1.2;
        char b = 'M';
        boolean e = true;

        System.out.println("Int:" + a);
        System.out.println("Double: " + c);
        System.out.println("Char: " + b);
        System.out.println("Boolean: " + e);

    }

}
